package main

import "fmt"

func main() {
	// String
	fmt.Println("Hello World")
	fmt.Println(len("Hello World"))
	fmt.Println("Hello World"[2]) //byte

	//number
	fmt.Println("Satu =", 1)
	fmt.Println("Dua =", 2)
	fmt.Println("Tiga koma lima =", 3.5)
	// bool
	fmt.Println("Benar =", true)
	fmt.Println("Salah =", false)
}
